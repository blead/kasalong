<?php
function convdate($datetime) {
  $dt = explode("-", $datetime);
  $y = $dt[0] + 543;
  $m = trim($dt[1], "0");
  $d = trim($dt[2], "0");
  $mlist = array(1=>"มกราคม", "กุมภาพันธ์", "มีนาคม", "เมษายน",
                 "พฤษภาคม", "มิถุนายน", "กรกฎาคม", "สิงหาคม",
                 "กันยายน", "ตุลาคม", "พฤศจิกายน", "ธันวาคม");
  return $d." ".$mlist[$m]." ".$y;
}
